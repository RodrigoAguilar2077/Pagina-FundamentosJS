// Declaracion de variables con let y const
// var en desuso

// variables let: cambiaran de valor
let age; // declaracion
console.log(age);
age= 23; // inicializacion
console.log(age);

let name = "Francisco"; // declaracion e inicializacion
console.log(name);
name = "Paco";
console.log(name);

// constantes const: no cambiaran de valor
const PI = 3.1416;
//PI = 3.15;
console.log(PI);